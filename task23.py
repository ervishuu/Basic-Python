#A value is one of the basic things a program works with, like a letter or a number. The values we have seen so far are 1, 2, and "Hello, World!"


message = "How are you"
n = 23
pi = 3.14

#A statement is an instruction that the Python interpreter can execute.
print(message)
print(n)
print(pi)

'''An expression is a combination of values, variables, and operators.
If you type an expression on the command line,
the interpreter evaluates it and displays the result'''

print("Expression")
print(n+pi)
print(n-pi)

'''Operators are special symbols that represent computations like
addition and multiplication.
The values the operator uses are called operands.'''

print("In below expression + is operator and n and pi is operands ")

'''When a variable name appears in the place of an operand, it is replaced with its
value before the operation is performed.'''
print('n+pi')
